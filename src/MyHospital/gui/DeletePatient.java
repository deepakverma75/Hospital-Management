    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        p=jcPid.getSelectedItem().toString();
        try
        {
        boolean result = PatientDao.deletePatient(p);
        if(result)
        {
            JOptionPane.showMessageDialog(null,"Patient Successfully Discharge!");
            loadReceptionistDetails();
        }
        else
          JOptionPane.showMessageDialog(null,"Some Error Patient not Discharge Please try Again","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Some Proble Occured Please try After Some time Internal Problem!","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

