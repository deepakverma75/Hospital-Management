     private void loadReceptionistDetails()
    {
        try
        {
        patient = PatientDao.getPatientid();
        if(patient.size()==0) {
            btnDelete.setEnabled(false);
            JOptionPane.showMessageDialog(null,"No Patient Available!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        btnDelete.setEnabled(true);
        Collection keys=patient.keySet();
        Iterator<String>itr = keys.iterator();
        jcPid.removeAllItems();
        while(itr.hasNext())
        {
            jcPid.addItem(itr.next());
        }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Some error Occured in DB","Error",JOptionPane.ERROR_MESSAGE);
   
        }
    }

