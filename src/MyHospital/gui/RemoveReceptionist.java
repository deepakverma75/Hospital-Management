    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        try
        {
            
            empid= jcEmpid.getSelectedItem().toString();
            boolean a = ReceptionistDao.removeReceptionist(empid);
            boolean b = EmpDao.empRemove(empid);

            if(a==true||b==true)
            {
                JOptionPane.showMessageDialog(null,"Employee Successfully Deleted!","Success",JOptionPane.INFORMATION_MESSAGE);
                loadEmpid();
                clear();
            }
            else
            JOptionPane.showMessageDialog(null,"Employee Not Deleted! Soory","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Some Poblem in DB","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jcEmpidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpidActionPerformed
        // TODO add your handling code here:
        if(jcEmpid.getSelectedItem()==null)
        return;
        empid = jcEmpid.getSelectedItem().toString();
        
        System.out.println(empid);
        try
        {
            UserPojo p =ReceptionistDao.getReceptionist(empid);
            txtUserid.setText(p.getUserid());
            txtUsername.setText(p.getUserName());
            txtPassword.setText(p.getPassword());
            txtUsertype.setText(p.getUsertype());

        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Some Exception occur while fetching Data","Erroe",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jcEmpidActionPerformed

