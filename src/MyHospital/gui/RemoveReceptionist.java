        public void loadEmpid()
    {
        try
        {
            
             jcEmpid.removeAllItems();
            ArrayList<UserPojo> id=ReceptionistDao.getAllId();
            id =ReceptionistDao.getAllId(); //EmpDao.getAllId();
            if(id.size()==0) {
            btnDelete.setEnabled(false);
            JOptionPane.showMessageDialog(null,"No unregistered receptionist present","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        btnDelete.setEnabled(true);
            for(UserPojo e:id)
            {
             jcEmpid.addItem(e.getEmpId());
            }
        }
        catch(SQLException e)
        {
            System.out.println("Some Problem in DB");
        }
    }
